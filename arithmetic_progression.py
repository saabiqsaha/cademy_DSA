class ArithmeticProgression:
    def __init__(self, first = int, second = int, difference = int):
        self.first = first
        self.second = second
        self.difference = difference

    def first_val(self, first):
        self.first = first
    def second_val(self, second):
        self.second = second
    def diff(self, difference):
        self.difference = difference

    
